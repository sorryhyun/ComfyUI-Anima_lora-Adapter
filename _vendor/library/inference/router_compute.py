"""Single-source-of-truth for the pure-compute kernels that run inside the
HydraLoRA / FeRA / σ-band router at inference time.

The training side, the inference loop, and the ComfyUI custom node
(``custom_nodes/comfyui-hydralora/``) all need to compute exactly the same
FEI / σ / σ-band math against the same trained router weights. Any drift
between the three copies shows up directly as wrong gates — the router
weights were trained against a specific functional form, so a permuted band
ordering or a different sinusoidal frequency schedule produces silently
corrupted samples with no exception raised.

This module is the *one* place each kernel lives. Both the live training tree
(`networks/lora_modules/router_state.py`, `library/runtime/fei.py`) and the
custom node import from here. The node ships a vendored copy of this file
plus its transitive deps under ``_vendor/`` so it stays standalone when not
sitting next to the anima_lora repo; the vendor tree is regenerated by
``scripts/sync_vendor.py``.

Public surface:

* ``gaussian_blur_2d`` — separable 2D Gaussian on ``[B, C, H, W]``.
* ``compute_fei_2band(z, sigma_low) -> (B, 2)`` simplex
  ``[e_low, e_high]``. The default FEI for ``library/runtime/fei.py`` and
  the plan2 ``stacked_experts_global_fei`` GlobalRouter.
* ``compute_fei_nband_high_to_low(z, sigma_low, num_bands) -> (B, N)``
  simplex ordered ``[high, ..., low]``. Legacy / author-faithful FeRA
  (``networks.methods.fera``, retired at plan2 — checkpoints still load
  through the node). Different ordering from ``compute_fei_2band``; do not
  unify the two.
* ``fei_sigma_low(h_lat, w_lat, div) -> float`` — bucket-invariant σ_low
  scaling (``min(H_lat, W_lat) / div``).
* ``sigma_sinusoidal_features(sigma, dim)`` — sinusoidal σ features matching
  the DiT t_embedder functional form.
* ``apply_sigma_band_mask`` — mask out-of-band expert logits to -inf for
  the σ-band partition routing.
* ``fei_temperature(fei, tau) -> simplex`` — ChimeraHydra hardwired-FEI freq
  gate ``normalize(FEI ** (1/τ))``. Used when ``freq_router_mode="fei"`` (no
  FreqRouter MLP on disk); τ=1.0 returns FEI unchanged.

The node also imports ``_FREQS_CACHE`` indirectly via this module; do not
expose it as part of the public surface — it's an implementation detail of
``sigma_sinusoidal_features``.
"""

from __future__ import annotations

from typing import List

import torch

from library.runtime.fei import (
    compute_fei_2band,
    fei_sigma_low,
    gaussian_blur_2d,
)

# The σ helpers live in router_state.py for now because of their tight
# coupling to the buffer-protocol machinery there (`_register_*`,
# `_set_*`). We re-export the pure math under public names — the buffer
# helpers stay private to router_state.
from networks.lora_modules.router_state import (
    _apply_sigma_band_mask as apply_sigma_band_mask,
    _fei_temperature as fei_temperature,
    _sigma_sinusoidal_features as sigma_sinusoidal_features,
)


def compute_fei_nband_high_to_low(
    z: torch.Tensor, sigma_low: float, num_bands: int
) -> torch.Tensor:
    """Return ``(B, num_bands)`` simplex energies, ordered ``[high, ..., low]``.

    Bit-identical to the now-retired
    ``networks/methods/fera.py::FrequencyEnergyIndicator``: bands are
    differences of adjacent pyramid levels (high-freq first), followed by
    the coarsest LP as the residual low-band; σ scales double outward
    from ``sigma_low``. Promoted to fp32 internally — squared norms can
    underflow at small energies in bf16.

    Author-faithful FeRA router weights were trained against this exact
    ordering, so any permutation here would corrupt the gate at
    inference. The plan2 ``stacked_experts_global_fei`` format uses
    ``compute_fei_2band`` instead — different ordering by design, do not
    unify.
    """
    z = z.float()
    sigmas: List[float] = [sigma_low * (2.0**k) for k in range(num_bands - 1)]
    pyramid = [z]
    for s in sigmas:
        pyramid.append(gaussian_blur_2d(pyramid[-1], s))
    bands = [pyramid[k] - pyramid[k + 1] for k in range(num_bands - 1)]
    bands.append(pyramid[-1])
    energies = torch.stack([b.pow(2).flatten(1).sum(-1) for b in bands], dim=-1)
    return energies / energies.sum(dim=-1, keepdim=True).clamp_min(1e-12)


